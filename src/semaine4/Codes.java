package semaine4;

public class Codes {
    public static void main(String[] args) {
        // char to Unicode
        char a = '*';
        char b = '9';
        char c = 'W';
        System.out.println((int) a + " " + (int) b + " " + (int) c);

        // Unicode to char
        int d = 34;
        int e = 65;
        int f = 123;
        System.out.println((char) d + " " + (char) e + " " + (char ) f);
    }
}
